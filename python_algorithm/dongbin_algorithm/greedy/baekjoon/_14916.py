
n = int(input())

num = n % 5
count = 0

# 거스름돈 1이나 3일 경우에는 계산할 수 없으므로 -1출력
if n == 1 or n == 3:
    print(-1)
    exit(0)
# 5로 나눈 값이 2로 나눠 떨어지면
elif num % 2 == 0:
    # 5로 나눈 동전 개수와 2로 나눈 동전 개수를 합해서 출력
    print(n//5 + num//2)
    exit(0)

# 2로 나눠 떨어지지 않으면, 5원 코인에서 1개를 빼고
# 다시 나머지에 5를 더하여 2와 5로 나눈 동전 개수를 합해줌
else:
    print((n//5)-1 + (num+5)//2)
    exit(0)
